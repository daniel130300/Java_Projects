    private void tableProductosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableProductosMouseClicked
            int seleccion = this.tableProductos.rowAtPoint(evt.getPoint()); 
            Comunicacion comunicacion = new Comunicacion();
            Comunicacion.setId_producto(Integer.parseInt(String.valueOf(tableProductos.getValueAt(seleccion, 0))));
            Comunicacion.setNombre(String.valueOf(tableProductos.getValueAt(seleccion, 1)));
            Comunicacion.setPrecio(String.valueOf(tableProductos.getValueAt(seleccion, 2)));
            this.dispose();
    }//GEN-LAST:event_tableProductosMouseClicked

